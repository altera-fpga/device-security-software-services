package com.intel.bkp.bkps.config;

import com.intel.bkp.bkps.utils.H2ConfigurationHelper;
import com.intel.bkp.core.utils.ProfileConstants;
import lombok.extern.slf4j.Slf4j;
import org.springframework.context.annotation.Bean;
import org.springframework.context.annotation.Configuration;
import org.springframework.context.annotation.Profile;
import org.springframework.data.jpa.repository.config.EnableJpaRepositories;
import org.springframework.transaction.annotation.EnableTransactionManagement;

import java.sql.SQLException;

@Configuration
@EnableJpaRepositories("com.intel.bkp.bkps.repository")
@EnableTransactionManagement
@Slf4j
public class DatabaseConfiguration {

    /**
     * Open the TCP port for the H2 database, so it is available remotely.
     *
     * @return the H2 database TCP server
     * @throws SQLException if the server failed to start
     */
    @Bean(initMethod = "start", destroyMethod = "stop")
    @Profile(ProfileConstants.SPRING_PROFILE_H2_CONSOLE)
    public Object h2TCPServer() throws SQLException {
        log.debug("Starting H2 database");
        return H2ConfigurationHelper.createServer("9092");
    }

}
